dep 'apt' do
  met? do
    return trud if grep(/^# Generated by babushka/, '/etc/apt/sources.list')
    ask("Sould we change to simple source list for Debian? (y/n):") == "y"
  end

  meet do
    '/etc/apt/sources.list'.p.copy('/etc/apt/sources.list.backup')
    render_erb "apt/sources.list.#{`cat /etc/debian_version`.split('/').first}", 
      :to => '/etc/apt/sources.list'
  end
  after { sudo 'apt-get update -y' }
end
